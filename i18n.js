export const I18N_LANGS = [['en', 'English'], ['fr', 'Français'], ['es', 'Español'], ['de', 'Deutsch'], ['it', 'Italiano'], ['pt', 'Português (BR)'], ['ja', '日本語'], ['ru', 'Русский'], ['pl', 'Polski'], ['zh-CN', '简体中文']];

// Map a raw navigator.language value to a supported language code.
// Chinese needs the region: zh, zh-CN, zh-SG, zh-Hans… map to zh-CN, and
// zh-TW/zh-HK (traditional) also fall back to zh-CN — better than English
// for those users until a dedicated zh-TW table exists. Other languages
// match on the 2-letter primary subtag.
export function resolveNavLang(raw) {
  const l = String(raw || 'en').toLowerCase();
  if (l.startsWith('zh')) return 'zh-CN';
  const short = l.slice(0, 2);
  return I18N[short] ? short : 'en';
}

// This whole block is DOM/browser glue (localStorage, navigator, document) —
// exercised by the e2e suite, not by the Node unit tests (same treatment as
// app.js/worker.js/gallerypage.js, see sonar.coverage.exclusions).
/* node:coverage disable */
function detectLang() {
  try {
    const saved = localStorage.getItem('lang');
    if (saved && I18N[saved]) return saved;
  } catch { /* storage may be unavailable */ }
  return resolveNavLang((typeof navigator !== 'undefined' && navigator.language) || 'en');
}

// the active language stays private and mutable; consumers read it through
// currentLang() so no mutable binding is ever exported (sonar S6861)
let activeLang = typeof document !== 'undefined' ? detectLang() : 'en';
export function currentLang() { return activeLang; }

// Translate a key in the current language; {placeholders} are filled from params.
export function t(key, params) {
  let s = I18N[activeLang]?.[key] ?? I18N.en[key] ?? key;
  if (params) for (const k of Object.keys(params)) s = s.replaceAll('{' + k + '}', params[k]);
  return s;
}

// Apply translations to all elements carrying data-i18n / data-i18n-title.
export function applyI18n() {
  document.documentElement.lang = activeLang;
  document.querySelectorAll('[data-i18n]').forEach((el) => { el.textContent = t(el.dataset.i18n); });
  document.querySelectorAll('[data-i18n-title]').forEach((el) => { el.title = t(el.dataset.i18nTitle); });
  document.querySelectorAll('[data-i18n-aria]').forEach((el) => { el.setAttribute('aria-label', t(el.dataset.i18nAria)); });
  document.querySelectorAll('[data-i18n-ph]').forEach((el) => { el.setAttribute('placeholder', t(el.dataset.i18nPh)); });
  document.querySelectorAll('[data-i18n-label]').forEach((el) => { el.setAttribute('label', t(el.dataset.i18nLabel)); });
  // biome option labels (biomes.js)
  document.querySelectorAll('[data-biome]').forEach((el) => { el.textContent = biomeLabel(el.dataset.biome); });
}

export function setLang(lang) {
  if (!I18N[lang]) return;
  activeLang = lang;
  try { localStorage.setItem('lang', lang); } catch { /* ignore */ }
  applyI18n();
}
